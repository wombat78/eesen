#include "base/kaldi-common.h"
#include "util/common-utils.h"
#include "net/net.h"

int main(int argc, char *argv[]) {
  try {
    using namespace kaldi;
    typedef kaldi::int32 int32;

    const char *usage =
        "Convert model format to the non-parallel version. This only involves changes the layer marker \n"
        "e.g., from <BiLstmParallel> to <BiLstm>. This conversion enables decoding with the model."
        "Usage:  format-to-nonparallel [options] <model-in> <model-out>\n"
        "e.g.:\n"
        "format-to-nonparallel --binary=true nnet.iter13 final.nnet\n";


    bool binary_write = true;
    
    ParseOptions po(usage);
    po.Register("binary", &binary_write, "Write output in binary mode");

    po.Read(argc, argv);

    if (po.NumArgs() != 2) {
      po.PrintUsage();
      exit(1);
    }

    std::string model_in_filename = po.GetArg(1),
        model_out_filename = po.GetArg(2);

    // Read the model
    Net net; 
    {
      bool binary_read;
      Input ki(model_in_filename, &binary_read);
      net.Read(ki.Stream(), binary_read);
    }

    // Save the model
    {
      Output ko(model_out_filename, binary_write);
      net.WriteNonParal(ko.Stream(), binary_write);
    }

    KALDI_LOG << "Written model to " << model_out_filename;
    return 0;
  } catch(const std::exception &e) {
    std::cerr << e.what() << '\n';
    return -1;
  }
}


